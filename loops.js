document.write("<h2>For Loop</h2>");
document.write("<ol>");
for(var i=1; i<=10; i++) {
    document.write("<li> i am Aniket </li>");
}
document.write("</ol>");

document.write("<h2>Do While Loop</h2>");
document.write("<ol>");
var a = 1;
do {
    document.write("<li> Full stack Development");
    a++;
} while(a <= 10);
document.write("</ol>");
document.write("<h2>While Loop</h2>");
document.write("<ol>");
var x = 1;
while(x <= 10) {
    document.write("<li> Welcome </li>");
    x++;
}
document.write("</ol>");